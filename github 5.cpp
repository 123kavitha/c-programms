#include <stdio.h>
 int main()
 {
   int a;
   long b;
   short c;
   float d;
   long double e;
   printf("enter int");
   scanf("%d,&a");
   printf("a");
   printf("enter short");
   scanf("%d,&b");
   printf("b");
   printf("enter double");
   scanf("%lf,&c");
   printf("c");
   printf("enter float");
   scanf("%f,&d");
   printf("d");
   printf("enter long double");
   scanf("%Lf,&e");
   printf("e");
 }
